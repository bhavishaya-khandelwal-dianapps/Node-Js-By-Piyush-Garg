//* What are HTTP Methods ? 
//? GET, POST, PUT, PATCH, DELETE 


//? GET -> When you want to get some data from the server.

//? POST -> When you want to send and mutate some data in server.

//? PUT -> When we want to upload some files like image, video, etc to the server then we use PUTY request.

//? PATCH -> When we want to change something like if we want to change our userName on instagram, facebook etc. 

//? DELETE -> When we want to delete something from the database. 